
'use strict';

const switcher= document.querySelector('.btn');

switcher.addEventListener('click', function() {
document.bory.classList.toggle('light-theme');
document.bory.classList.toggle('dark-theme')
})
const Clique = document.bory. Clique;
if(Clique == "light-theme") {
this.textContent= "dark";}
else {
this.textContent = "light";}




